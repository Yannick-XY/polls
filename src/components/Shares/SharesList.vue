<template>
	<ConfigBox v-bind="configBoxProps.sharesList">
		<template #icon>
			<ShareIcon />
		</template>

		<UserSearch class="add-share" />
		<ShareItemAllUsers v-if="sessionStore.appPermissions.allAccess" />
		<SharePublicAdd v-if="sessionStore.appPermissions.publicShares" />

		<div v-if="sharesStore.active.length" class="shares-list shared">
			<TransitionGroup is="div"
				name="list"
				:css="false">
				<ShareItem v-for="(share) in sharesStore.active"
					:key="share.id"
					:share="share"
					@show-qr-code="openQrModal(share)" />
			</TransitionGroup>
		</div>

		<NcModal v-if="qrModal" size="small" @close="qrModal=false">
			<QrModal :name="pollStore.configuration.title"
				:description="pollStore.configuration.description"
				:encode-text="qrText"
				class="modal__content">
				<template #description>
					<MarkUpDescription />
				</template>
			</QrModal>
		</NcModal>
	</ConfigBox>
</template>

<script>
import { mapStores } from 'pinia'
import { NcModal } from '@nextcloud/vue'
import { ConfigBox, QrModal } from '../Base/index.js'
import ShareItem from './ShareItem.vue'
import UserSearch from '../User/UserSearch.vue'
import SharePublicAdd from './SharePublicAdd.vue'
import ShareItemAllUsers from './ShareItemAllUsers.vue'
import ShareIcon from 'vue-material-design-icons/ShareVariant.vue'
import MarkUpDescription from '../Poll/MarkUpDescription.vue'
import { t } from '@nextcloud/l10n'
import { usePollStore } from '../../stores/poll.ts'
import { useSharesStore } from '../../stores/shares.ts'
import { useSessionStore } from '../../stores/session.ts'

export default {
	name: 'SharesList',

	components: {
		ShareIcon,
		UserSearch,
		ConfigBox,
		SharePublicAdd,
		ShareItemAllUsers,
		ShareItem,
		QrModal,
		NcModal,
		MarkUpDescription,
	},

	data() {
		return {
			qrModal: false,
			qrText: '',
			configBoxProps: {
				sharesList: {
					name: t('polls', 'Locked shares (read only access)'),
				},
			},

		}
	},

	computed: {
		...mapStores(usePollStore, useSharesStore, useSessionStore),
	},

	methods: {
		openQrModal(share) {
			this.qrText = share.URL
			this.qrModal = true
		},
	},
}
</script>

